// The other half of an if statement, is the else. Code in between the
// curly braces of the else statement will run if the expression in the
// parentheses is false.
//
// The previous example of:
// if (10 < 5) {
//   console.log('this doesnt happen')
// }
// could be expanded to:
// if (10 < 5) {
//   console.log('this doesnt happen')
// } else {
//   console.log('this will happen!')
// }
//
// Try writing an if else statement like the one above and change the
// expression in the parentheses to watch the different blocks of code
// get run at different times.


